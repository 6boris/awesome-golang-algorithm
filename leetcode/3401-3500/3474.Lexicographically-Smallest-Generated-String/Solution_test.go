package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name       string
		str1, str2 string
		expect     string
	}{
		{"TestCase1", "TFTF", "ab", "ababa"},
		{"TestCase2", "TFTF", "abc", ""},
		{"TestCase3", "F", "d", "a"},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.str1, c.str2)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v %v",
					c.expect, got, c.str1, c.str2)
			}
		})
	}
}

