package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name      string
		mass      int
		asteroids []int
		expect    bool
	}{
		{"TestCase1", 5, []int{4, 9, 23, 4}, false},
		{"TestCase2", 10, []int{3, 9, 19, 5, 21}, true},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.mass, c.asteroids)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v %v",
					c.expect, got, c.mass, c.asteroids)
			}
		})
	}
}

