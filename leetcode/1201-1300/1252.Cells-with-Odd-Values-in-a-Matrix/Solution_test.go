package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name    string
		n, m    int
		indices [][]int
		expect  int
	}{
		{"TestCase", 2, 3, [][]int{{0, 1}, {1, 1}}, 6},
		{"TestCase", 2, 2, [][]int{{1, 1}, {0, 0}}, 0},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.n, c.m, c.indices)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v %v %v",
					c.expect, got, c.n, c.m, c.indices)
			}
		})
	}
}

