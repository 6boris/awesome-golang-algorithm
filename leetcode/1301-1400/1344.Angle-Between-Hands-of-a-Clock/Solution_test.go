package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name          string
		hour, minutes int
		expect        float64
	}{
		{"TestCase1", 12, 30, 165},
		{"TestCase2", 3, 30, 75},
		{"TestCase3", 3, 15, 7.5},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.hour, c.minutes)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v %v",
					c.expect, got, c.hour, c.minutes)
			}
		})
	}
}

