package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name     string
		value, k int
		consec   []int
		expect   []bool
	}{
		{"TestCase1", 4, 3, []int{4, 4, 4, 3}, []bool{false, false, true, false}},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.value, c.k, c.consec)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v %v %v",
					c.expect, got, c.value, c.k, c.consec)
			}
		})
	}
}

