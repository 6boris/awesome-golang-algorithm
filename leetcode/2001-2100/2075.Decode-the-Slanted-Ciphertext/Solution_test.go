package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name        string
		encodedText string
		rows        int
		expect      string
	}{
		{"TestCase1", "ch   ie   pr", 3, "cipher"},
		{"TestCase2", "iveo    eed   l te   olc", 4, "i love leetcode"},
		{"TestCase3", "coding", 1, "coding"},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.encodedText, c.rows)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v %v",
					c.expect, got, c.encodedText, c.rows)
			}
		})
	}
}

