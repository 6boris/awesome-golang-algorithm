package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name   string
		p, q   int
		expect int
	}{
		{"TestCase1", 3, 1, 1},
		{"TestCase2", 6, 2, 1},
		{"TestCase3", 4, 3, 2},
		{"TestCase4", 3, 2, 0},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.p, c.q)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v %v",
					c.expect, got, c.p, c.q)
			}
		})
	}
}

