package Solution

import (
	"reflect"
	"strconv"
	"testing"
)

func TestSolution(t *testing.T) {
	//	测试用例
	cases := []struct {
		name   string
		inputs []int
		h      int
		expect int
	}{
		{"TestCase1", []int{3, 6, 7, 11}, 8, 4},
		{"TestCase2", []int{30, 11, 23, 4, 20}, 5, 30},
		{"TestCase3", []int{30, 11, 23, 4, 20}, 6, 23},
	}

	//	开始测试
	for i, c := range cases {
		t.Run(c.name+" "+strconv.Itoa(i), func(t *testing.T) {
			got := Solution(c.inputs, c.h)
			if !reflect.DeepEqual(got, c.expect) {
				t.Fatalf("expected: %v, but got: %v, with inputs: %v",
					c.expect, got, c.inputs)
			}
		})
	}
}

